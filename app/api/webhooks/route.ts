import { NextRequest, NextResponse } from "next/server";
import { Octokit } from "octokit";

const OPENAI_API_KEY = process.env.OPENAI_API_KEY;
const GH_TOKEN = process.env.GITHUB_TOKEN;
const GH_OWNER = "vlaskov67";
const GH_REPO = "vlaskov-store";

export async function POST(req: NextRequest) {
  try {
    const event = req.headers.get("x-github-event");
    if (event !== "issues") return NextResponse.json({ ignored: true });

    const payload = await req.json();
    if (payload.action !== "opened") return NextResponse.json({ skipped: true });

    const title = payload.issue.title;
    const body = payload.issue.body || "";
    const issueNumber = payload.issue.number;

    console.log("üì• –ù–æ–≤–∞—è –∑–∞–¥–∞—á–∞:", title);

    // 1. –ß—Ç–µ–Ω–∏–µ –≤—Å–µ—Ö –¥–æ–∫—É–º–µ–Ω—Ç–æ–≤ –∏–∑ –ø–∞–ø–∫–∏ docs —Ä–µ–ø–æ–∑–∏—Ç–æ—Ä–∏—è —Å–∞–π—Ç–∞
    const octokit = new Octokit({ auth: GH_TOKEN });
    const docsRes = await octokit.rest.repos.getContent({
      owner: GH_OWNER,
      repo: GH_REPO,
      path: "docs",
    });

    let docsContent = "";

    if (Array.isArray(docsRes.data)) {
      for (const file of docsRes.data) {
        if (file.download_url) {
          const content = await fetch(file.download_url).then((res) => res.text());
          docsContent += `–§–∞–π–ª: ${file.name}\n${content}\n\n---\n\n`;
        }
      }
    }

    // 2. –ü—Ä–∞–≤–∏–ª—å–Ω—ã–π –ø—Ä–æ–º–ø—Ç –¥–ª—è OpenAI
    const prompt = `
–¢—ã –æ–ø—ã—Ç–Ω—ã–π —Ä–∞–∑—Ä–∞–±–æ—Ç—á–∏–∫ –Ω–∞ Laravel, Livewire –∏ Alpine.js. –ò—Å–ø–æ–ª—å–∑—É–π —Å–ª–µ–¥—É—é—â–µ–µ —Ç–µ—Ö–Ω–∏—á–µ—Å–∫–æ–µ –∑–∞–¥–∞–Ω–∏–µ:

${docsContent}

–¢–≤–æ—è –∑–∞–¥–∞—á–∞:
${title}

${body}

–°–æ–∑–¥–∞–π –Ω—É–∂–Ω—ã–µ —Ñ–∞–π–ª—ã –∏ –ø–∞–ø–∫–∏ –¥–ª—è —Ä–µ–∞–ª–∏–∑–∞—Ü–∏–∏ –∑–∞–¥–∞—á–∏. –û—Ç–≤–µ—Ç –¥–∞–π —Å—Ç—Ä–æ–≥–æ –≤ —Ñ–æ—Ä–º–∞—Ç–µ:

–ø—É—Ç—å/–∫/—Ñ–∞–π–ª—É.php
---
—Å–æ–¥–µ—Ä–∂–∏–º–æ–µ —Ñ–∞–π–ª–∞

–°–ª–µ–¥—É—é—â–∏–π —Ñ–∞–π–ª...

–ë–µ–∑ –ª–∏—à–Ω–∏—Ö –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–µ–≤.
`;

    const completion = await fetch("https://api.openai.com/v1/chat/completions", {
      method: "POST",
      headers: {
        "Content-Type": "application/json",
        Authorization: `Bearer ${OPENAI_API_KEY}`,
      },
      body: JSON.stringify({
        model: "gpt-4o",
        messages: [{ role: "user", content: prompt }],
        temperature: 0.2,
      }),
    }).then((res) => res.json());

    const answer = completion.choices?.[0]?.message?.content || "// –ø—É—Å—Ç–æ";
    const branchName = `auto/issue-${issueNumber}`;

    const mainRef = await octokit.rest.git.getRef({
      owner: GH_OWNER,
      repo: GH_REPO,
      ref: "heads/main",
    });

    await octokit.rest.git.createRef({
      owner: GH_OWNER,
      repo: GH_REPO,
      ref: `refs/heads/${branchName}`,
      sha: mainRef.data.object.sha,
    });

    // 3. –°–æ–∑–¥–∞–Ω–∏–µ —Ñ–∞–π–ª–æ–≤ –∏ –∫–æ–º–º–∏—Ç–∞
    const files = answer.split("\n\n–°–ª–µ–¥—É—é—â–∏–π —Ñ–∞–π–ª...\n\n");
    const treeItems = [];

    for (const file of files) {
      const [filePath, fileContent] = file.split("\n---\n");
      const blob = await octokit.rest.git.createBlob({
        owner: GH_OWNER,
        repo: GH_REPO,
        content: fileContent,
        encoding: "utf-8",
      });

      treeItems.push({ path: filePath.trim(), mode: "100644", type: "blob", sha: blob.data.sha });
    }

    const tree = await octokit.rest.git.createTree({
      owner: GH_OWNER,
      repo: GH_REPO,
      base_tree: mainRef.data.object.sha,
      tree: treeItems,
    });

    const commit = await octokit.rest.git.createCommit({
      owner: GH_OWNER,
      repo: GH_REPO,
      message: `auto: resolve issue #${issueNumber}`,
      tree: tree.data.sha,
      parents: [mainRef.data.object.sha],
    });

    await octokit.rest.git.updateRef({
      owner: GH_OWNER,
      repo: GH_REPO,
      ref: `heads/${branchName}`,
      sha: commit.data.sha,
      force: true,
    });

    await octokit.rest.pulls.create({
      owner: GH_OWNER,
      repo: GH_REPO,
      title: `auto: resolve #${issueNumber}`,
      head: branchName,
      base: "main",
      body: "ü§ñ –ê–≤—Ç–æ–º–∞—Ç–∏—á–µ—Å–∫–∏ —Å–æ–∑–¥–∞–Ω–Ω—ã–π PR –ø–æ –≤–∞—à–µ–º—É issue.",
    });

    return NextResponse.json({ status: "PR created" });
  } catch (err) {
    console.error("‚ùå –û—à–∏–±–∫–∞ –≤ —Ñ–∞–±—Ä–∏–∫–µ –∫–æ–¥–∞:", err);
    return NextResponse.json({ error: true, message: (err as Error).message });
  }
}
